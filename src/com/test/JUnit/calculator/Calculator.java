package com.test.JUnit.calculator;

/**
 * a Calculator class
 */
public class Calculator {
    /**
     * Add
     * @param a int a
     * @param b int b
     * @return result
     */
    public int add(int a, int b) {
        return a - b;
    }

    /**
     * Minus
     * @param a int a
     * @param b int b
     * @return result
     */
    public int sub(int a, int b) {
        return a - b;
    }
}
